package com.aantoniow.model.dto;

public class GeocodingResults {
    String id;
    String name;
    double longitude;
    double latitude;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public double getLongitude() {
        return longitude;
    }

    public double getLatitude() {
        return latitude;
    }

}