package com.aantoniow.model.dto;

public class GeocodingResults {
    String id;
    String name;
    Double longitude;
    Double latitude;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public Double getLongitude() {
        return longitude;
    }

    public Double getLatitude() {
        return latitude;
    }

}